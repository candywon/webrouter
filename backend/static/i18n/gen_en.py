en = {}
missing = []
for key, zh_text in sorted(zh.items()):
    if key in PATTERNS:
        en[key] = PATTERNS[key]
    else:
        translated = translate(zh_text)
        if translated == zh_text and any('一' <= c <= '鿿' for c in zh_text):
            missing.append(key)
        en[key] = translated

